export function setAutocompleteSelection(address, country) {
    var addy = {
        line1: undefined,
        line2: undefined,
        line3: undefined,
        city: undefined,
        state: undefined,
        postal: undefined
    }
    let startcountry = local.getItem("userCountry");
    let shippingCountry = startcountry.split('~')
    console.log(country);
    if (country === "USA") {
        let split = address.split(/, | - /);
        addy.line1 = split[0];
        addy.city = split[1];
        addy.state = split[2];
        addy.postal = split[4]
    }
    if (country === "India") {
        let split = address.split(/, | - /);
        addy.line1 = split[0] + " " + split[1];
        addy.state = split[split.length - 3]
        addy.postal = split[split.length - 1]
        addy.city = split.slice(2,-3).toString()
    }
    return addy
}